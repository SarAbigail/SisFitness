    private void ValidaCampos()
    {
        boolean result=false;
        if (txtFecha.getText().isEmpty())
            JOptionPane.showMessageDialog(null, "Debe de ingresar la Fecha", "Aviso", JOptionPane.WARNING_MESSAGE);
        else if (clienteSel==null)
            JOptionPane.showMessageDialog(null, "Debe seleccionar al Cliente", "Aviso", JOptionPane.WARNING_MESSAGE);
        else if (tablaDetalle.getRowCount()==0)
            JOptionPane.showMessageDialog(null, "Debe de ingresar Productos en la grilla", "Aviso", JOptionPane.WARNING_MESSAGE);   

    }
