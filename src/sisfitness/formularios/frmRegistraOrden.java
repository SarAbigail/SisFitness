    private void btnRemoveProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductoActionPerformed
        // TODO add your handling code here:
        int selectedRow = gridDetalle.getSelectedRow();
        if (selectedRow != -1) {
            tablaDetalle.removeItem(selectedRow);
            tablaDetalle.actualizarNroItems();
        }
    }//GEN-LAST:event_btnRemoveProductoActionPerformed

    private void addRow(ProductoModel productoSel)
    {
           int nItem=0;
            OrdenVentaDetalleModel item=new OrdenVentaDetalleModel();
            if (tablaDetalle.getRowCount()==0)
                nItem=1;
            else
                nItem=tablaDetalle.getRowCount() + 1;
              
            item.setProducto(productoSel);
            item.setItem(nItem);
            item.setCantidad(1);
            item.setPrecio(productoSel.getPrecio());         
            item.setTotal(item.getCantidad() * item.getPrecio());
            tablaDetalle.addItem(item);
    }

