    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        
    // TODO add your handling code here:
        
        int selectedRow = gridCategoria.getSelectedRow();
        if (selectedRow != -1) {
            int id = (int) gridCategoria.getValueAt(selectedRow, 0);
            CategoriaModel categoria=new CategoriaModel();
            categoria=bcategoria.BuscarByID(id);
            frmCategoria frmcate=new frmCategoria(this, rootPaneCheckingEnabled,OPCION_MODIFICAR,categoria);
            frmcate.setVisible(true);
            if (frmcate.isResultstatus())
            {
                ListarCategorias();
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = gridCategoria.getSelectedRow();
        if (selectedRow != -1) {

            int nResultMsg =JOptionPane.showConfirmDialog(null, "Â¿Desea eliminar el registro seleccionado?","Aviso",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
            if (nResultMsg==JOptionPane.YES_OPTION)
            {
                int id = (int) gridCategoria.getValueAt(selectedRow, 0);
                CategoriaModel categoria=new CategoriaModel();
                categoria.setId(id);
                int nResult=bcategoria.GuardarCategoria(OPCION_ELIMINAR, categoria);
                if (nResult>0)
                {
                    ListarCategorias(); 
                }
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

